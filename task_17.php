<?php

class Calculator
{
    public function summ($a, $b)
    {
        return $a + $b;
    }

    public function difference($a, $b)
    {
        return $a - $b;
    }

    public function multiplication($a, $b)
    {
        return $a * $b;
    }

    public function division($a, $b)
    {
        return $a / $b;
    }
}